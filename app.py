    if cv2 is None:
        return jsonify(error="OpenCV not available"), 503
    source = IP_CAM_URL if IP_CAM_URL else int((request.get_json(silent=True) or {}).get("camera_index", 0))
    cap = cv2.VideoCapture(source)

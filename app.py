        rows, _ = exec_sql(conn, "SELECT id FROM users WHERE username = " + placeholder(), ("admin",))
        if not rows:
            exec_sql(conn, "INSERT INTO users (username, password_hash) VALUES (" + qmarks(2) + ")", ("admin", password_hash("admin123"))); conn.commit()

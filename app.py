    if IP_CAM_URL:
        source = IP_CAM_URL
    else:
        # Local camera only works when you run the app on a machine with /dev/video0
        source = int(request.args.get("camera_index", 0))
    return Response(gen_frames(source), mimetype="multipart/x-mixed-replace; boundary=frame")

